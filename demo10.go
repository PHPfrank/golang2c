package main

import (
	"fmt"
)

//古典问题（兔子生崽）：有一对兔子，从出生后第3个月起每个月都生一对兔子，小兔子长到第三个月后每个月又生一对兔子，
//假如兔子都不死，问每个月的兔子总数为多少？（输出前40个月即可）

func main () {
	
	m1 := 1
	m2 := 1
    for i := 1 ; i <= 20 ; i++ {
		
		fmt.Printf("%12d%12d",m1,m2);
        if(i%2 == 0) {
		  fmt.Printf("\n");
		}
		m1 = m1 + m2;
		m2 = m1 + m2;
	}

}